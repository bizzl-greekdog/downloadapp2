namespace DownloadApp\App\UserBundle\Exception;


use Throwable;

/**
 * Class NoLoggedInUserException
 * @package DownloadApp\App\UserBundle\Exception
 */
class NoLoggedInUserException extends \Exception
{
    const CODE = 0x39a99e3c;

    /**
     * Construct the exception. Note: The message is NOT binary safe.
     * @link http://php.net/manual/en/exception.construct.php
     * @param string $message [optional] The Exception message to throw.
     * @param int $code [optional] The Exception code.
     * @param Throwable $previous [optional] The previous throwable used for the exception chaining.
     * @since 5.1.0
     */
    public function __construct(Throwable $previous = null)
    {
        parent::__construct('No user logged in', self::CODE, $previous);
    }

}
